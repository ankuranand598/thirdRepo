/* A Grade - Excellent

B Grade - Very Good

C Grade - Good

D Grade - Needs a lot of Improvement

F Grade - Failed */
var grades = "D";
switch (grades) {
    case "A":
        console.log("Excellent");
        break;
    case "B":
        console.log("Very Good");
        break;
    case "C":
        console.log("Good");
        break;
    case "D":
        console.log("Needs a lot of improvement");
        break;
    case "F":
        console.log("Failed");
        break;
    default:
        console.log("Please check your input");
}
